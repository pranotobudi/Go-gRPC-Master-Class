	if err != nil {
		log.Fatalf("could not connect: %v", err)
	}
	defer cc.Close()
	c := greetpb.NewGreetServiceClient(cc)
	// doUnary(c)
	// doServerClient(c)
	// doClientStreaming(c)
	doBiDiStreaming(c)

